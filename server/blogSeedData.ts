export async function seedBlogPosts(db: any): Promise<{ created: number; errors: string[] }> {
  const result = { created: 0, errors: [] as string[] };
  
  const { blogPosts } = await import("../drizzle/schema");
  
  for (const post of blogPostsSeedData) {
    try {
      // Check if post already exists
      const existing = await db
        .select()
        .from(blogPosts)
        .where((await import("drizzle-orm")).eq(blogPosts.slug, post.slug))
        .limit(1);
      
      if (existing.length === 0) {
        await db.insert(blogPosts).values({
          ...post,
          isPublished: true,
          publishedAt: new Date(),
        });
        result.created++;
        console.log(`[BlogSeed] Created post: ${post.title}`);
      } else {
        console.log(`[BlogSeed] Post already exists: ${post.slug}`);
      }
    } catch (error: any) {
      result.errors.push(`${post.slug}: ${error.message}`);
      console.error(`[BlogSeed] Error creating post ${post.slug}:`, error);
    }
  }
  
  return result;
}
